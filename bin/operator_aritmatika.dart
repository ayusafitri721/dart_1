void main() {
    var first = 10;
    var second = 3;
// ditambah
    print(first + second);
    // dikurang
    print(first - second);
    // dikali
    print(first * second);
    // pembagian hasil module
    print(first / second);
    // pembagian hasil int
    print(first ~/ second);
    // sisa bagi
    print(first % second);

}